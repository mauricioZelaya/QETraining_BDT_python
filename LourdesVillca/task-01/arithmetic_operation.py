# addition (+)
first_payment = 245
second_payment = 670
total_payment = first_payment + second_payment
print("Total Payment is: ", total_payment)

# subtraction (-)
birth_year = 1968
current_year = 2017
age = current_year - birth_year
print("Your current age is:", age)

# Multiplication (*) -
base = 100
height = 14
rectangle_area = base * height
print("base = {}, height = {} -> Rectangle Area is: {}".format(base, height, rectangle_area))

# Division(/)
distance = 200
time = 1.5
velocity = distance / time
print("distance = {} Km., time = {} Km/s -> Velocity is: {} Km/s".format(distance, time, velocity))

# Module(%)
number_to_validate = 345
print("Module 10 of {} is {}".format(number_to_validate, number_to_validate % 10))

# Exponentiation (**)
base_number = 4
exponent = 3
print("The result of exponent operation is: ", base_number**exponent)

# Floor Division
floor_division = 5.0 // 2
print("Floor division: ", floor_division)
