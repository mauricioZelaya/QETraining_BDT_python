def calculate_age_minutes(age):
    """
    Method to calculate the age in minutes.

    :param age:     int     Age to calculate.
    :return:        int     Age represented in minutes
    """
    return age * 365 * 24 * 60


def calculate_age_hours(age):
    """
    Method to calculate the age in hours.

    :param age:     int     Age to calculate.
    :return:        int     Age represented in hours
    """
    return age * 365 * 24


def calculate_age_days(age):
    """
    Method to calculate the age in days.

    :param age:     int     Age to calculate.
    :return:        int     Age represented in days
    """
    return age * 365
