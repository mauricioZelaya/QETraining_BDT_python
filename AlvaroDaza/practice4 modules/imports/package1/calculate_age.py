def calculate_age_minutes(age):
    """
    This method calculates the age in days.
    :param age:    int   age in years.
    :return:       int   age in minutes.
    """
    return age * 365 * 24 * 60


def calculate_age_hours(age):
    """
    This method calculates the age in hours.
    :param age:    int   age in years.
    :return:       int   age in hours.
    """
    return age * 365 * 24


def calculate_age_days(age):
    """
    This method calculates the age in days.
    :param age:    int   age in years.
    :return:       int   age in days.
    """
    return age * 365
