def ageToMonths(age):
    return age * 12


def ageToDays(age):
    return (age * 12) * 365


def ageToHours(age):
    return (age * 12) * 365 * 24


def ageToMinutes(age):
    return (age * 12) * 365 * 24 * 60


def ageToSeconds(age):
    return (age * 12) * 365 * 24 * 60 * 60

